piece_values = {
    chess.PAWN: 100,
    chess.KNIGHT: 320,
    chess.BISHOP: 330,
    chess.ROOK: 500,
    chess.QUEEN: 900,
    chess.KING: 0
}

def evaluate_board(board):
    if board.is_checkmate():
        if board.turn:
            return -9999  # Human wins
        else:
            return 9999   # AI wins

    if board.is_stalemate():
        return 0  # Draw

    value = 0

    # Material + Position Evaluation
    for piece_type in piece_values:
        white_pieces = board.pieces(piece_type, chess.WHITE)
        black_pieces = board.pieces(piece_type, chess.BLACK)

        value += len(white_pieces) * piece_values[piece_type]
        value -= len(black_pieces) * piece_values[piece_type]

        # Piece-square bonuses
        for square in white_pieces:
            value += piece_square_tables[piece_type][square]

        for square in black_pieces:
            value -= piece_square_tables[piece_type][chess.square_mirror(square)]

    # Mobility
    value += len(list(board.legal_moves)) * 0.1

    return value

# Minimax with Alpha-Beta
def minimax(board, depth, is_maximizing, alpha, beta):
    if depth == 0 or board.is_game_over():
        return evaluate_board(board)

    if is_maximizing:
        max_eval = -math.inf
        for move in board.legal_moves:
            board.push(move)
            eval = minimax(board, depth - 1, False, alpha, beta)
            board.pop()
            max_eval = max(max_eval, eval)
            alpha = max(alpha, eval)
            if beta <= alpha:
                break
        return max_eval
    else:
        min_eval = math.inf
        for move in board.legal_moves:
            board.push(move)
            eval = minimax(board, depth - 1, True, alpha, beta)
            board.pop()
            min_eval = min(min_eval, eval)
            beta = min(beta, eval)
            if beta <= alpha:
                break
        return min_eval

# Get best move
def get_best_move(board, depth):
    best_move = None
    max_eval = -math.inf

    for move in board.legal_moves:
        board.push(move)
        eval = minimax(board, depth - 1, False, -math.inf, math.inf)
        board.pop()

        if eval > max_eval:
            max_eval = eval
            best_move = move

    return best_move
